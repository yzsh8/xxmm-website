&nbsp;&nbsp;&nbsp;&nbsp;就在十二个小时前，我与交往两年的异地女友正式分了手，为了显示出与以往的分别有所不同，这次她只送我到了地铁口，她快速且坚决的转身，没有回头。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;此时我盯着窗外漆黑一片的夜景，完全没有思维活动。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;七天前，当她说出分手时，我第一个不眠夜就此开始。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;我订了第二天的飞机，赶来挽回视若瑰宝的她以及这份如同荣耀的恋情，到目前为止，我这七天的睡眠时间加起来都不到九个小时，夜里我在无人的街道不停地徘徊，腿却毫无酸痛感，每天吃的也很少，我觉得自己已经完完全全做到了言情剧里的感情失败者的全部要素，说实话，真的感觉不到一点困倦，脑袋始终一片空白，心像被钢琴弦缠绕，时不时绞痛一下，终于明白情感是可以影响体能。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;如今，我坐在火车里，结局以不可挽回的姿态迅速且坚定地降临。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;玻璃映出乘警经过的身影，好像他已经在我身边经过很多遍了，为了不给周围人添麻烦，我决定还是躺下吧。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;躺在卧铺上，依旧没有睡意，根据这几天的经验，我知道最后我会睡着，半个小时？也许今天会长一点吧……醒来时，是另一个天地。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;我还未睁开眼睛，就已发觉有些不对，没有列车的晃动感，没有浆洗床铺的味道，空间彷佛也不再那幺压抑，一股熟悉的发香撩动着我的呼吸。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;我勐地睁开了眼睛，迅速坐起身，环视着周围。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;这是……是寝室，借着蒙蒙亮的晨光，我辨认着周遭景致，除了我还有三个床位，似乎大家都在睡觉，我低头看着自己，一件熟悉的睡衣映入眼帘。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;我悚然大惊，余光瞥到床头有面小镜子，我颤抖地拿了起来，望向镜中人。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;是我刚刚分手的女友。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;一觉过后，我变成了我的前女友。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;不需要掐大腿去确认，因为此刻周围环境传来的感觉是任何梦境都模拟不了的真实，包括身体刚苏醒的感觉，让我真切地体会到是我的意识对这具躯体的主导。<br><br>
